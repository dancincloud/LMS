    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) studentJTable.getModel();

        model.setRowCount(0);
        for (User user : course.getStudentDirectory().getList()) {
            Object[] row = new Object[3];
            row[0] = user.getId();
            row[1] = user.getName();
            if(user.getType()==0)
                row[2] = "Student";
            else if(user.getType()==1)
                row[2] = "Instructor";

            model.addRow(row);
        }
        
    }

